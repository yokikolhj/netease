    private static ExecutorService threadPool = Executors.newCachedThreadPool(); // 多线程用线程池简单示例

    public static void main(String[] args) throws Exception {
        // 具体执行之前一定会有加载项目信息【类加载 配置文件】
        Map<String, WebXml> projectInfos = ProjectUtil.load();

        ServerSocket serverSocket = new ServerSocket(8080); // os->jvm->JDK API提供开发人员的
        System.out.println("tomcat 服务器启动成功");
        while (!serverSocket.isClosed()) {
            // 获取新连接（请求，响应）（浏览器/httpClient发起的请求）
            Socket socket = serverSocket.accept(); // 阻塞

            threadPool.submit(() -> {
                // 接收数据，打印
                InputStream inputStream = socket.getInputStream(); // 读取请求数据的request
                System.out.println("收到请求： ");
                BufferedReader reader = new BufferedReader(new InputStreamReader(inputStream, "utf-8"));
                String msg;
                StringBuffer requestInfo = new StringBuffer();
                while ((msg = reader.readLine()) != null) {
                    if (msg.length() == 0) {
                        break;
                    }
                    requestInfo.append(msg).append("\r\n");
                }
                System.out.println("--------------收到请求：" + requestInfo.toString());
                // TODO 调用项目业务代码
                // GET /tomcat-servlet-demo/index HTTP/1.1
                // 1.找到对应的项目 tomcat-servlet-demo
                String firstLine = requestInfo.toString().split("\r\n")[0]; // 获取第一行数据
                String projectName = firstLine.split(" ")[1].split("/")[1]; //找项目名称

                // 2.知道执行哪一个servlet /index
                String servletPath = firstLine.split(" ")[1].replace("/" + projectName, "");

                // 3.执行servlet【难点：一个运行中的jvm去执行某一个class里面的业务代码】
                String servletName = projectInfos.get(projectName).servletMapping.get(servletPath);
                Servlet servlet = projectInfos.get(projectName).servletInstances.get(servletName);

                HttpServletRequest servletRequest = createRequest(requestInfo.toString());
                HttpServletResponse servletResponse = createResponse();

                servlet.service(servletRequest, servletResponse);

                // http响应结果 200
                OutputStream outputStream = socket.getOutputStream(); // 返回响应的request
                outputStream.write("HTTP/1.1 200 OK\r\n".getBytes());
                outputStream.write("Content-Length: 11\r\n\n".getBytes());
                outputStream.write("Hello World".getBytes());
                outputStream.flush();
                socket.close();

                // 返回内容
                return null;
            });
        }
        serverSocket.close();
    }

